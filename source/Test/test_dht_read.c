#include <stdlib.h>

#include "test_dht_read.h"

int test_dht_read(int type, int pin, float* humidity, float* temperature) {
  // Validate humidity and temperature arguments and set them to zero.
  if (humidity == NULL || temperature == NULL) {
    return -1;
  }
  *temperature = 42.0f;
  *humidity = 50.0f;

  return 0;
}
